# Sentinel for DeepCore rows in ``mat_ax2`` (two axes only in sub-images).
_DC_AX2_SENTINEL = -500


def build_ic86_cnn_mapping(
    string_label: str,
    dom_number_label: str,
) -> pd.DataFrame:
    """Build the IceCube-86 CNN pixel mapping DataFrame.

    Places the 78 main-array strings on a fixed 10x10 grid and folds the
    eight DeepCore strings (79..86) into upper/lower DeepCore views using
    the usual DOM numbering convention.

    Args:
        string_label: Column name for IceCube string number (1..86).
        dom_number_label: Column name for DOM number (1..60).

    Returns:
        DataFrame indexed by (``string_label``, ``dom_number_label``) with
        voxel index columns ``mat_ax0``, ``mat_ax1``, and ``mat_ax2``.
    """
    rows: List[Tuple[int, int, int, int, float]] = []

    for string in range(1, 79):
        ax0, ax1 = _IC86_STRING_TO_AX01[string]
        for dom_number in range(1, 61):
            rows.append((string, dom_number, ax0, ax1, float(dom_number - 1)))

    for string in range(79, 87):
        ax0 = string - 79
        for dom_number in range(1, 11):
            rows.append(
                (string, dom_number, ax0, dom_number - 1, _DC_AX2_SENTINEL)
            )
        for dom_number in range(11, 61):
            rows.append(
                (string, dom_number, ax0, dom_number - 11, _DC_AX2_SENTINEL)
            )

    df = pd.DataFrame(
        rows,
        columns=[
            string_label,
            dom_number_label,
            MAT_AX0_COL,
            MAT_AX1_COL,
            MAT_AX2_COL,
        ],
    )
    df.sort_values(
        by=[string_label, dom_number_label],
        ascending=[True, True],
        inplace=True,
    )
    df.set_index([string_label, dom_number_label], inplace=True, drop=False)
    return df


